public class Ch7_3_StaticVsInstance {
    /*
    Static:
        - Class'a aittir.
        - Erişebilmek için bir objeye ihtiyaç duymaz
        - ClassName.VariableName ya da ClassName.MethodName olarak erişilebilir

    Instance (non-static):
        - Objeye aittir
        - Erişebilmek için bir objeye ihtiyaç duyulur
        - objectName.VariableName ya da objectName.MethodName olarak erişilebilir.

    Ne zaman instance ne zaman static? Eğer düşündüğün yapıyı gözünün önüne obje olarak getirebiliyorsan o zaman instance,
        getiremiyorsan veya gerek yok diyorsan o zaman static
     */


}
